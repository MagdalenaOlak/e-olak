<p>Analityczne myślenie wg. Encyklopedii Zarządzania, jest zbiorem określonych umiejętności i warunków, które konieczne są do wykonywania pracy analityka biznesowego. Składają się na nie między innymi kreatywne myślenie, uczenie się, trafne podejmowanie decyzji, myślenie wizualne, koncepcyjne i systemowe.</p>
<p>W ogłoszeniach o pracę pracodawcy coraz częściej życzą sobie, aby kandydaci posiadali  umiejętności analityczne. Czym jednak właściwie są? Czy faktycznie są one potrzebne w codziennej pracy testera? Czy można się ich nauczyć? Jak je rozwijać?</p>
<p>Dla wielu osób określenie "umiejętności analityczne" jest bardzo enigmatyczne, a to one właśnie, a dokładnie  myślenie krytyczno-analityczne, jest jednym z trzech typów myślenia, które składa się na inteligencję człowieka.  Nie są one jednak niczym innym jak umiejętnością zrozumienia i rozwiązywania problemów na podstawie informacji, do których mamy dostęp. Zasadniczo polegają na odwoływaniu się do faktów, które zostały przez nas zapamiętane. W pracy wszelkie zadania polegające na szukaniu różnic i podobieństw, wygłaszaniu krytyki, ocenianiu,  poszukiwaniu i wyjaśnianiu różnych zjawisk oraz dokonywaniu analizy, wymagają analitycznego myślenia.</p>
 <p>Jak można wywnioskować - osoba posiadająca umiejętności analityczne jest doskonałym obserwatorem, zauważa pewne niuanse i szczegóły,  oraz ma szeroką perspektywę i potrafi łączyć fakty.</p>
<p>To właśnie sprawia, że umiejętności analityczne mają w pracy testera zasadnicze znaczenie. Pracodawcom zależy na tym, by pracownik potrafił analizować dane, wyciągać z nich wnioski oraz podejmować trafne decyzje (oparte o wcześniej przeprowadzony przegląd informacji).  Ważna jest także umiejętność zrozumienia różnorodności przypadków, w których działać będzie dane oprogramowanie. Ponadto umiejętność dojścia po nitce do kłębka, od problemu aż do jego przyczyny, pozwala testerom na szybsze wykrycie większej ilości bugów. Dobry tester potrafi znaleźć błędy (choć z pewnością nie wszystkie, ponieważ to niemożliwe), ale świetny znajduje ich przyczyny i zna sposoby, aby im zapobiec. Już w trakcie czytania specyfikacji, czy uczestnictwa w początkowych przeglądach  można zidentyfikować mnóstwo wieloznaczności, ogólników, pomyłek i braków, które da się wyeliminować przed implementacją kodu, a tym samym zapobiec powstawaniu błędów w dalszych etapach projektu, co znacznie obniża koszty  - jednak to temat na odrębny artykuł.</p>
<p>Jednym słowem analityczne myślenie pozwala rozłożyć na części i zrozumieć działanie funkcjonalności testowanej aplikacji na każdym poziomie, czy w każdym typie testów, co jest niezbędne do efektywnego przeprowadzania testów.</p>
<p>Jeśli wydaje ci się, że nie do końca jest ok, z Twoim analitycznym myśleniem, nie martw się, jest to umiejętność, którą można rozwijać i doskonalić przez całe życie, a która przyda ci się nie tylko w pracy.</p>
<p>Bardzo ważne w myśleniu analitycznym jest to, by nasz umysł był aktywny, stale dowiadywał się nowych rzeczy, poszerzał horyzonty. Aby ćwiczyć nasz mózg warto ciągle uczyć się czegoś nowego. Idealnym sposobem na to, by polepszyć nasze umiejętności, jest czytanie książek, uczenie się języków obcych (np. język angielski niezbędny w pracy testera) rozwiązywanie wszelkiego rodzaju łamigłówek czy krzyżówek, a nawet zadań matematycznych. Ponadto możesz wykorzystywać wolne chwile, na przykład drogę do pracy, na ćwiczenia mózgu przy pomocy aplikacji na telefon, wśród których bezproblemowo znajdziesz treningi mózgu, czyli gry, które nas angażują i wymagają logiki czy analitycznego myślenia. W Internecie można znaleźć wiele podpowiedzi jak ćwiczyć analityczne myślenie.</p>
<p>Ja, dla przykładu, od kilku lat codziennie poświęcam od kilkudziesięciu minut, do nawet kilku godzin ( w zależności od czasu) na czytanie w języku angielskim.  Na początku było to czytanie jedynie książek beletrystycznych, teraz co raz częściej są to książki branżowe, instrukcje, czy poradniki. Poza tym stale się czegoś uczę i korzystam z kursów w języku angielskim.</p>
<p>Jest wiele sposobów na to by połączyć przyjemne z pożytecznym. Ty też możesz znaleźć coś dla siebie - do czego cię bardzo zachęcam.</p>

