<p>Analityczne myślenie wg. Encyklopedii Zarządzania, jest zbiorem określonych umiejętności i&nbsp;warunków, które konieczne są do wykonywania pracy analityka biznesowego. Składają się na nie między innymi kreatywne myślenie, uczenie się, trafne podejmowanie decyzji, myślenie wizualne, koncepcyjne i&nbsp;systemowe.</p>
<p>W ogłoszeniach o pracę pracodawcy coraz częściej życzą sobie, aby kandydaci posiadali  umiejętności analityczne. Czym jednak właściwie są? Czy faktycznie są one potrzebne w&nbsp;codziennej pracy testera? Czy można się ich nauczyć? Jak je rozwijać?</p>
<p>Dla wielu osób określenie "umiejętności analityczne" jest bardzo enigmatyczne, a&nbsp;to one właśnie, a&nbsp;dokładnie  myślenie krytyczno-analityczne, jest jednym z&nbsp;trzech typów myślenia, które składa się na inteligencję człowieka.  Nie są one jednak niczym innym jak umiejętnością zrozumienia i&nbsp;rozwiązywania problemów na podstawie informacji, do których mamy dostęp. Zasadniczo polegają na odwoływaniu się do faktów, które zostały przez nas zapamiętane. W pracy wszelkie zadania polegające na szukaniu różnic i&nbsp;podobieństw, wygłaszaniu krytyki, ocenianiu,  poszukiwaniu i&nbsp;wyjaśnianiu różnych zjawisk oraz dokonywaniu analizy, wymagają analitycznego myślenia.</p>
<p>Jak można wywnioskować - osoba posiadająca umiejętności analityczne jest doskonałym obserwatorem, zauważa pewne niuanse i&nbsp;szczegóły,  oraz ma szeroką perspektywę i&nbsp;potrafi łączyć fakty.</p>
<p>To właśnie sprawia, że umiejętności analityczne mają w pracy testera zasadnicze znaczenie. Pracodawcom zależy na tym, by pracownik potrafił analizować dane, wyciągać z&nbsp;nich wnioski oraz podejmować trafne decyzje (oparte o&nbsp;wcześniej przeprowadzony przegląd informacji).  Ważna jest także umiejętność zrozumienia różnorodności przypadków, w&nbsp;których działać będzie dane oprogramowanie. Ponadto umiejętność dojścia po nitce do kłębka, od problemu aż do jego przyczyny, pozwala testerom na szybsze wykrycie większej ilości bugów. Dobry tester potrafi znaleźć błędy (choć z pewnością nie wszystkie, ponieważ to niemożliwe), ale świetny znajduje ich przyczyny i&nbsp;zna sposoby, aby im zapobiec. Już w trakcie czytania specyfikacji, czy uczestniczenia w&nbsp;początkowych przeglądach można zidentyfikować mnóstwo wieloznaczności, ogólników, pomyłek i&nbsp;braków, które da się wyeliminować przed implementacją kodu, a tym samym zapobiec powstawaniu błędów w&nbsp;dalszych etapach projektu, co znacznie obniża koszty - jednak to temat na odrębny artykuł.</p>
<p>Krótko mówiąc analityczne myślenie pozwala rozłożyć na części i&nbsp;zrozumieć działanie funkcjonalności testowanej aplikacji na każdym poziomie, czy w każdym typie testów, co jest niezbędne do efektywnego przeprowadzania testów.</p>
<p>Jeśli wydaje ci się, że nie do końca jest OK z&nbsp;twoim analitycznym myśleniem, nie martw się, jest to umiejętność, którą można rozwijać i&nbsp;doskonalić przez całe życie, a&nbsp;która przyda ci się nie tylko w&nbsp;pracy.</p>
<p>Bardzo ważne w&nbsp;myśleniu analitycznym jest to, by nasz umysł był aktywny, stale dowiadywał się nowych rzeczy, poszerzał horyzonty. Aby ćwiczyć nasz mózg warto ciągle uczyć się czegoś nowego. Idealnym sposobem na to, by polepszyć nasze umiejętności, jest czytanie książek, uczenie się języków obcych (np. język angielski niezbędny w pracy testera) rozwiązywanie wszelkiego rodzaju łamigłówek czy krzyżówek, a&nbsp;nawet zadań matematycznych. Ponadto możesz wykorzystywać wolne chwile, na przykład drogę do pracy, na ćwiczenia mózgu przy pomocy aplikacji na telefon, wśród których bezproblemowo znajdziesz treningi mózgu, czyli gry, które nas angażują i&nbsp;wymagają logiki czy analitycznego myślenia. W&nbsp;Internecie można znaleźć wiele podpowiedzi jak ćwiczyć analityczne myślenie.</p>
<p>W moim przypadku, od kilku lat, idealnym sposobem na trening mózgu, jest codziennie czytanie w&nbsp;języku angielskim, na co poświęcam od kilkudziesięciu minut, do nawet kilku godzin (w&nbsp;zależności od czasu). Na początku było to czytanie jedynie instrukcji, czy poradników dotyczących głównie budowy stron internetowych (szczególnie PHP), potem weszły książki beletrystyczne. Na końcu pojawiły się w kręgu moich zainteresowań książki branżowe. Poza tym stale się czegoś uczę i&nbsp;korzystam z&nbsp;kursów on-line w&nbsp;języku angielskim. Polecam www.udemy.com, czy Youtube.</p>
